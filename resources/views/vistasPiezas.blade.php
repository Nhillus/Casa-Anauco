                    <p class="container-escultura">
                        
                        El diseño del mobiliario en Venezuela, como en toda provincia de España, estaba siempre inspirado en la 
                        moda europea con las variantes e interpretaciones locales que le dan sus características propias sin embargo, 
                        la única excepción fue la creación del diseño original y local de un mueble caracterizado por un espaldar y 
                        asiento inclinados para proporcionar una posición más cómoda al cuerpo humano. 
                        
                        Esta pieza de mobiliario se conoció desde el siglo XVII y aún se conoce con la voz original del cumanagoto BUTACA.

                        Hacia finales del siglo XVIII, las referencias del mundo clásico se reflejan en el arte como una reacción al estilo rococó. 
                        Los diseños tomaron un carácter arquitectónico y simétrico con predominio de la línea recta. El ebanista Serafín Antonio Almeida se 
                        destaca como uno de los impulsores tanto del nuevo diseño del mobiliario como de las técnicas para su elaboración. Se descarta la 
                        talla y se le dá más importancia a la madera pulida. Las estructuras de cedro se recuieron entonces con chapas de gateado 
                        incrustrándolas con filetes y otros adornos hechos en madera de carreto. Almeida aparece como abanderado de la llamada Escuela de 
                        Marquetería de Caracas, la cual desarrolló el estilo neoclásico con una interpretación local muy particular.
