    <h1>arquitectura</h1>

 <h3 text-align="justify">
 El diseño de la Quinta de Anauco, sede del Museo, corresponde a las típicas construcciones de estancias ubicadas cercanas a la ciudad y sigue características de la arquitectura civil venezolana del siglo XVIII. En aquella época estaba rodeada de jardines ornamentales y algunas siembras de café, caña y árboles frutales.

La planta está constituída por un cuerpo central de forma rectangular, con dos patios interiores, corredores y dos caballerizas. Esta planta y su distribución derivan de las construcciones andaluzas.
 
<img src= {{asset ("imagenes/coleccion1.png")}} class="img-rounded" alt="Cinque Terre" width="400" height="400"> 
<br>
Los techos de toda la casa son de caña amarga sostenidos por viguetas de mangle. Sólo los techos de los corredores principales y el corredor exterior son de madera de cedro, con el sistema llamado de pares y nudillos, consistente en tablas sostenidas por vigas cuadradas. En el interior de las habitaciones se pueden observar los cielos rasos de yeso, característicos de fines del siglo XVIII. Los corredores están hechos utilizando el sistema adintelado. Este sistema se caracteriza por vigas horizontales apoyadas sobre zapatas, las cuales se apoyan a su vez sobre columnas cilíndricas inspiradas en el orden toscano.

<br>
<img src= {{asset ("imagenes/coleccion2.png")}} class="img-rounded" alt="Cinque Terre" width="400" height="400"> 
<br>
Las paredes fueron construídas con el sistema de tapia y rafa, consistente en tierra pisada con cal y piedra y refuerzos intercalados de cal y canto. En algunos casos las paredes están adornadas con murales y zócalos estarcidos o cintas de colores. 
<br>
Los pisos están cubiertos de panelas de ladrillo cocido de forma hexagonal o cuadrada. En los patios hay grandes piedras rosadas, llamadas de mollejón, traídas de España como lastre en los barcos. Las hojas de puertas y ventanas son de madera de cedro pintadas en dos tonos de verde. Se abren por medio de goznes de hierro y tienen cerraduras y pasadores también de hierro forjado.
<br>
<img src= {{asset ("imagenes/coleccion3.png")}} class="img-rounded" alt="Cinque Terre" width="400" height="400"> 
<br>
El adorno en la parte superior de las ventanas recibe el nombre de peinazos y es un modelo típico de la segunda mitad del siglo XVIII.
<br>
Las ventanas principales muestran rejas de hierro voladas apoyadas sobre repisas y cubiertas con un pequeño techo llamado guardapolvo. En su interior y en el espesor de la pared se encuentan los llamados poyos que son dos asientos ubicados a cada lado de la ventana.
<br>
<h1>pintura</h1>
<h3>
La colección de Pinturas del Museo de Arte Colonial de Caracas consta de unas 97 obras.
<br>
El arte de la pintura al principio del período hispánico se vió limitado por escaso número de pintores. En aquel momento predominaban los tonos oscuros, los negros, los rojos y las tierras. Aparte de los pintores de la capital, se localizan también escuelas importantes en El Tocuyo y en Mérida. Los sujetos se reparten entre el retrato, del cual el Museo tiene excelentes muestras, y el tema religioso.
<br>
<img src= {{asset ("imagenes/coleccion4.png")}} class="img-rounded" alt="Cinque Terre" width="400" height="400"> 
<br>
Destacan los nombres de Francisco José de Lerma y Villegas, José Lorenzo Zurita, Francisco Contreras y el prolífico Juan Pedro López , abuelo del gran humanista Don Andrés Bello, de quien el Museo tiene una buena representación de obras, y la llamada Escuela de los Landaeta.
</h3>
<h1>Escultura</h1>
<h3>
La escultura, siempre de tema religioso, tiene muestras de gran significación en las colecciones del Museo. Destacan los nombres de Juan Pedro López, también dedicado a la escultura y José Francisco Rodríguez, alias El Tocuyano.
</h3>
<h1>Mobiliario</h1>
<h3>
La colección de muebles que se exhibe en el Museo de Arte Colonial es la más extensa y representativa del período hispánico. Cuenta alrededor de 224 piezas y se divide en tres períodos: Barroco, Rococó y Neoclásico.
<h2>Siglo XVII</h2>
<br>
<h3>
A comienzo del siglo XVII los tipos de muebles eran muy limitados: se reducían a la cama, el banco, la silla, la mesa, el arcón y el escaparate, con un estilo derivado del barroco español con ciertas influencias mudéjares.
<br>
Eran muebles con proporciones chatas y cuadradas cuya característicaa principal era la austeridad y la severidad. Las mesas se hicieron primero con patas torneadas y luego con patas onduladas terminadas en espiral. Estas últimas eran llamadas "mesas de pies volteados". Los escaparates y las chambranas de las mesas, así como las hojas de ventanas y puertas, se adornaron con chaflanes o cuarterones.
<img src= {{asset ("imagenes/coleccion5.png")}} class="img-rounded" alt="Cinque Terre" width="400" height="400"> 
</h3>
<h2>Siglo XVIII</h2>
<h3>
Más adelante, con el creciente comercio impulsado por la Compañía Guipuzcoana, y con la presencia de una nueva inmigración, comienza a notarse la influencia de la moda francesa. Las líneas se hicieron más sinuosas y asimétricas, y las porciones más altas y elegantes, acordes con la altura de los techos. Los muebles pintados y dorados se popularizaron durante casi todo el siglo XVIII y su uso derivaba de la imitación de los muebles laqueados orientales. Eran hechos generalmente de cedro el cual, por su apropiada porosidad, podía recibir el yeso y luego ser pintado. En este trabajo de tallado y pintado de muebles, destaca en Caracas la figura de Domingo Gutiérrez, tallista y ebanista canario introductor del estilo rococó, cuya influencia se prolongó hasta los últimos años del siglo XVIII.
<br>
La Cama de Aparato era considerado el mueble más importante de la casa. No se conservan muestras de las camas de cuatro columnas con dosel y cortinas del siglo XVI ó XVII. El Museo exhibe en cambio una importante cama de la segunda mitad del siglo XVIII en la que el elemento principal resulta ser el copete, tallado, pintado y dorado como era la usanza en buena parte de todos los muebles de la época.
</h3>
<h2>La Butaca</h2>
<h3>
La Butaca .El diseño del mobiliario en Venezuela, como en toda provincia de España, estaba siempre inspirado en la moda europea con las variantes e interpretaciones locales que le dan sus características propias sin embargo, la única excepción fue la creación del diseño original y local de un mueble caracterizado por un espaldar y asiento inclinados para proporcionar una posición más cómoda al cuerpo humano. Esta pieza de mobiliario se conoció desde el siglo XVII y aún se conoce con la voz original del cumanagoto BUTACA.
<br>
<img src= {{asset ("imagenes/coleccion6.png")}} class="img-rounded" alt="Cinque Terre" width="400" height="400"> 
<br>
Hacia finales del siglo XVIII, las referencias del mundo clásico se reflejan en el arte como una reacción al estilo rococó. Los diseños tomaron un carácter arquitectónico y simétrico con predominio de la línea recta. El ebanista Serafín Antonio Almeida se destaca como uno de los impulsores tanto del nuevo diseño del mobiliario como de las técnicas para su elaboración. Se descarta la talla y se le dá más importancia a la madera pulida. Las estructuras de cedro se recubrieron entonces con chapas de gateado incrustrándolas con filetes y otros adornos hechos en madera de carreto. Almeida aparece como abanderado de la llamada Escuela de Marquetería de Caracas, la cual desarrolló el estilo neoclásico con una interpretación local muy particular.
</h3>
<h2>Plateria</h2>
<h3>
La platería constituyó una de las manifestaciones más importantes durante el período hispánico venezolano.
<br>
Desde la segunda mitad del siglo XVI, gran cantidad de plateros desarrollaron su arte, fundamentalmente en Caracas y alcanzaron su apogeo en la segunda mitad del siglo XVIII. Trabajaban con plata mexicana, negociada en intercambio por cacao.
<br>
<img src= {{asset ("imagenes/coleccion7.png")}} class="img-rounded" alt="Cinque Terre" width="400" height="400"> 
<br>
Fue una producción de piezas civiles y religiosas. Las piezas de orden civil desaparecieron casi por completo al ser fundidas en 1814 para sufragar los gastos de la Guerra de Independencia.Destacan los nombres de los plateros Sebastián de Ochoa, Pedro Ignacio Ramos, Domingo Tomás Núñez, Luis Vicente López y Pedro Fermín Arias, entre otros, todos agrupados en un solo gremio que permitió continuidad y tradición en la obra.Total 95 piezas.
</h3>
